package si.sintal.sintaltehnika.ui.main;

public class RegCasa {
    String datum;
    String casOd;
    String casDo;


    public String getDatum() {
        return datum;
    }
    public void setDatum(String newDatum) {
        this.datum = newDatum;
    }

    public String getCasOd() {
        return casOd;
    }
    public void setCasOd(String newCasOd) {
        this.casOd = newCasOd;
    }

    public String getCasDo() {
        return casDo;
    }
    public void setCasDo(String newCasDo) {
        this.casDo = newCasDo;
    }
}
